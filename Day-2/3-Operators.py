# 1. Arithmatic Operators
print(10+5)
print(10-5)
print(10*5)
print(10/5)
print(10//5)
print(10%5)


# 2. Relational Operators=> Boolean value
print(10>5)
print(10<5)
print(10>=5)
print(10<=5)
print(10==5)
print(10!=5)

# 3. Logical Operator

print(10>5 and 2<5) #True
print(10>5 and 2>5) #False

print(10>5 or 2<5) #True
print(10>5 or 2>5) #True

print(not(10>5))#True

